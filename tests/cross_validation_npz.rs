//! Cross-validation tests for `NPZ` parsing against `NumPy` reference.
//!
//! Validates `parse_npz` output against fixtures generated from the Gemma Scope
//! 2B SAE weights (`params.npz`, 5 `F32` arrays). The Python script
//! `tests/fixtures/npz_reference/generate_npz.py` creates the fixtures.

#![cfg(feature = "npz")]
#![allow(
    clippy::panic,
    clippy::unwrap_used,
    clippy::expect_used,
    clippy::indexing_slicing,
    clippy::as_conversions,
    clippy::cast_possible_truncation,
    clippy::similar_names,
    clippy::wildcard_enum_match_arm,
    clippy::float_cmp
)]

use std::time::Instant;

use anamnesis::{inspect_npz, inspect_npz_from_reader, parse_npz, NpzDtype};

// ---------------------------------------------------------------------------
// Reference fixture parsing
// ---------------------------------------------------------------------------

/// A single array's expected metadata and spot-check data from the binary
/// fixture generated by `generate_npz.py`.
struct RefArray {
    name: String,
    shape: Vec<u64>,
    dtype_id: u32,
    data: Vec<u8>,
}

/// Parse the binary reference fixture into a list of expected arrays.
///
/// Format (all LE): see `generate_npz.py` docstring.
fn parse_reference(data: &[u8]) -> Vec<RefArray> {
    let mut offset = 0;

    let num_arrays = read_u32(data, &mut offset);
    let mut arrays = Vec::with_capacity(num_arrays as usize);

    for _ in 0..num_arrays {
        let name_len = read_u32(data, &mut offset) as usize;
        let name = std::str::from_utf8(&data[offset..offset + name_len])
            .unwrap()
            .to_owned();
        offset += name_len;

        let num_dims = read_u32(data, &mut offset) as usize;
        let mut shape = Vec::with_capacity(num_dims);
        for _ in 0..num_dims {
            shape.push(read_u64(data, &mut offset));
        }

        let dtype_id = read_u32(data, &mut offset);

        let data_len = read_u32(data, &mut offset) as usize;
        let arr_data = data[offset..offset + data_len].to_vec();
        offset += data_len;

        arrays.push(RefArray {
            name,
            shape,
            dtype_id,
            data: arr_data,
        });
    }

    arrays
}

fn read_u32(data: &[u8], offset: &mut usize) -> u32 {
    let val = u32::from_le_bytes(data[*offset..*offset + 4].try_into().unwrap());
    *offset += 4;
    val
}

fn read_u64(data: &[u8], offset: &mut usize) -> u64 {
    let val = u64::from_le_bytes(data[*offset..*offset + 8].try_into().unwrap());
    *offset += 8;
    val
}

/// Map `NpzDtype` to the fixture's dtype ID (must match `generate_npz.py`).
fn dtype_to_id(dtype: NpzDtype) -> u32 {
    match dtype {
        NpzDtype::Bool => 0,
        NpzDtype::U8 => 1,
        NpzDtype::I8 => 2,
        NpzDtype::U16 => 3,
        NpzDtype::I16 => 4,
        NpzDtype::U32 => 5,
        NpzDtype::I32 => 6,
        NpzDtype::U64 => 7,
        NpzDtype::I64 => 8,
        NpzDtype::F16 => 9,
        NpzDtype::BF16 => 10,
        NpzDtype::F32 => 11,
        NpzDtype::F64 => 12,
        _ => panic!("unknown NpzDtype variant"),
    }
}

