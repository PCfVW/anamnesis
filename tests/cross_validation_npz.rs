/// Validate that `parse_npz` correctly reads a small `NPZ` generated from
/// the Gemma Scope 2B SAE weights. Checks array count, names, shapes,
/// dtypes, and byte-exact data match against the `NumPy` reference.
#[test]
fn cross_validate_gemma_scope_sae() {
    let fixture_path = concat!(
        env!("CARGO_MANIFEST_DIR"),
        "/tests/fixtures/npz_reference/gemma_scope_small.npz"
    );
    let reference_data = include_bytes!("fixtures/npz_reference/gemma_scope_reference.bin");

    // Parse reference fixture
    let expected = parse_reference(reference_data);
    assert_eq!(expected.len(), 5, "expected 5 arrays in reference");

    // Parse NPZ with anamnesis
    let start = Instant::now();
    let tensors = parse_npz(fixture_path).expect("parse_npz failed");
    let elapsed = start.elapsed();

    eprintln!(
        "Gemma Scope SAE (small): {} arrays, parsed in {:.1} µs",
        tensors.len(),
        elapsed.as_secs_f64() * 1e6
    );

    assert_eq!(
        tensors.len(),
        expected.len(),
        "array count mismatch: got {}, expected {}",
        tensors.len(),
        expected.len()
    );

    // Verify each array against reference
    for ref_arr in &expected {
        let tensor = tensors
            .get(&ref_arr.name)
            .unwrap_or_else(|| panic!("array '{}' not found in parse_npz output", ref_arr.name));

        // Name (redundant field matches key)
        assert_eq!(tensor.name, ref_arr.name, "name mismatch");

        // Shape: the small NPZ has flattened 1D arrays [8] (truncated from the
        // original shapes stored in ref_arr.shape). Verify element count matches.
        let expected_elements = 8usize;
        let actual_elements: usize = tensor.shape.iter().product();
        assert_eq!(
            actual_elements, expected_elements,
            "element count mismatch for '{}': shape {:?} = {actual_elements}, expected {expected_elements}",
            ref_arr.name, tensor.shape
        );

        // Log original shape from reference for diagnostics
        eprintln!(
            "  original shape: {:?} (truncated to [{expected_elements}])",
            ref_arr.shape
        );

        // Dtype
        let actual_dtype_id = dtype_to_id(tensor.dtype);
        assert_eq!(
            actual_dtype_id, ref_arr.dtype_id,
            "dtype mismatch for '{}': got {actual_dtype_id}, expected {}",
            ref_arr.name, ref_arr.dtype_id
        );

        // Data: byte-exact match
        assert_eq!(
            tensor.data.len(),
            ref_arr.data.len(),
            "data length mismatch for '{}': got {}, expected {}",
            ref_arr.name,
            tensor.data.len(),
            ref_arr.data.len()
        );
        assert_eq!(
            tensor.data,
            ref_arr.data,
            "data mismatch for '{}': first differing byte at position {}",
            ref_arr.name,
            tensor
                .data
                .iter()
                .zip(ref_arr.data.iter())
                .position(|(a, b)| a != b)
                .unwrap_or(0)
        );

        eprintln!(
            "  {}: shape={:?}, dtype={}, {} bytes OK",
            ref_arr.name,
            tensor.shape,
            tensor.dtype,
            tensor.data.len()
        );
    }
}

/// Phase 4.7 substrate-equivalence: `inspect_npz_from_reader` over the same
/// archive bytes loaded into memory must return an `NpzInspectInfo` that is
/// field-for-field identical to `inspect_npz` over the file on disk. This is
/// the behavioural contract HTTP-range adapters depend on — the substrate
/// (file vs. cursor vs. range-backed adapter) must not change the metadata.
#[test]
fn inspect_path_and_reader_agree_on_gemma_scope_fixture() {
    let fixture_path = concat!(
        env!("CARGO_MANIFEST_DIR"),
        "/tests/fixtures/npz_reference/gemma_scope_small.npz"
    );

    let path_info = inspect_npz(fixture_path).expect("inspect_npz failed");

    let bytes = std::fs::read(fixture_path).expect("read fixture failed");
    let reader_info = inspect_npz_from_reader(std::io::Cursor::new(&bytes))
        .expect("inspect_npz_from_reader failed");

    eprintln!(
        "Gemma Scope SAE inspect parity: {} arrays, {} bytes total",
        reader_info.tensors.len(),
        reader_info.total_bytes
    );

    assert_eq!(
        path_info.tensors.len(),
        reader_info.tensors.len(),
        "tensor count mismatch between path-based and reader-based inspect"
    );
    assert_eq!(
        path_info.total_bytes, reader_info.total_bytes,
        "total_bytes mismatch between path-based and reader-based inspect"
    );
    assert_eq!(
        path_info.dtypes, reader_info.dtypes,
        "dtypes vec mismatch between path-based and reader-based inspect"
    );

    for (a, b) in path_info.tensors.iter().zip(reader_info.tensors.iter()) {
        assert_eq!(a.name, b.name, "tensor name mismatch");
        assert_eq!(a.shape, b.shape, "shape mismatch for tensor '{}'", a.name);
        assert_eq!(a.dtype, b.dtype, "dtype mismatch for tensor '{}'", a.name);
        assert_eq!(
            a.byte_len, b.byte_len,
            "byte_len mismatch for tensor '{}'",
            a.name
        );
    }
}
